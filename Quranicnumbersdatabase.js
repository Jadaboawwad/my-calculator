// دالة للحصول على معلومات الرقم من قاعدة البيانات
export const getNumberInfo = (number) => {
  // تحويل الرقم لنص في حال كان رقماً
  const key = String(number);
  return quranicNumbersDatabase[key] || null;
};

// دالة للحصول على أقرب رقم موجود في قاعدة البيانات
export const getNearestNumberInfo = (number) => {
  // استثناء الكسور والألفاظ الخاصة
  const numericKeys = Object.keys(quranicNumbersDatabase)
    .filter(key => !isNaN(key))
    .map(Number)
    .sort((a, b) => a - b);
  
  if (numericKeys.length === 0) return null;
  
  // إيجاد أقرب رقم
  let nearest = numericKeys[0];
  let minDiff = Math.abs(number - nearest);
  
  for (const num of numericKeys) {
    const diff = Math.abs(number - num);
    if (diff < minDiff) {
      minDiff = diff;
      nearest = num;
    }
  }
  
  return {
    number: nearest,
    info: quranicNumbersDatabase[String(nearest)],
    difference: number - nearest
  };
};

// دالة للبحث عن أرقام متعددة
export const getMultipleNumbersInfo = (numbers) => {
  return numbers.map(num => {
    const info = getNumberInfo(num);
    return {
      number: num,
      info: info || getNearestNumberInfo(num)
    };
  }).filter(item => item.info !== null);
};

// دالة لتصنيف الأعداد حسب نوعها
export const classifyNumber = (number) => {
  if (number === 3 || number === 6 || number === 9) {
    return 'tesla'; // أرقام تسلا المقدسة
  } else if (number === 7 || number % 10 === 7) {
    return 'blessed'; // الرقم المبارك
  } else if (number >= 1 && number <= 10) {
    return 'fundamental'; // الأعداد الأساسية
  } else if (number >= 11 && number <= 19) {
    return 'compound'; // الأعداد المركبة
  } else if (number % 10 === 0 && number >= 20 && number <= 90) {
    return 'decade'; // ألفاظ العقود
  } else if (number >= 100) {
    return 'large'; // الأعداد الكبيرة
  }
  return 'other';
};

// دالة لحساب طاقة الرقم
export const calculateNumberEnergy = (number) => {
  const classification = classifyNumber(number);
  const info = getNumberInfo(number);
  
  let energyLevel = 'medium';
  let energyDescription = 'طاقة متوازنة';
  
  if (classification === 'tesla') {
    energyLevel = 'very_high';
    energyDescription = 'طاقة عالية جداً - رقم تسلا المقدس';
  } else if (classification === 'blessed') {
    energyLevel = 'blessed';
    energyDescription = 'طاقة مباركة - رقم مبارك';
  } else if (info && info.verses) {
    // تحليل طاقة الآيات
    const hasHighEnergy = info.verses.some(v => v.energy === 'high' || v.energy === 'powerful');
    const hasWarning = info.verses.some(v => v.energy === 'warning' || v.energy === 'critical');
    
    if (hasHighEnergy) {
      energyLevel = 'high';
      energyDescription = 'طاقة إيجابية عالية';
    } else if (hasWarning) {
      energyLevel = 'warning';
      energyDescription = 'وقت حذر وانتباه';
    }
  }
  
  return {
    level: energyLevel,
    description: energyDescription,
    classification: classification
  };
};

